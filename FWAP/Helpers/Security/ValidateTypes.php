namespace FWAP\Helpers\Security;

class ValidateTypes
{
    private  $theValue, $theType, $theDefinedValue = "", $theNotDefinedValue = "";

    public static function getSQLValueString($theValue, $theType, $theDefinedValue = "", $theNotDefinedValue = "")
    {
        if (PHP_VERSION < 6) {
            $theValue = get_magic_quotes_gpc() ? stripslashes($theValue) : $theValue;
        }

        $theValue = function_exists("htmlspecialchars") ? htmlspecialchars($theValue) : htmlspecialchars($theValue);

        switch ($theType) {
            case "text":
                $theValue = ($theValue != "" ) ? "$theValue"  : "NULL";
                break;
            case "email":
            $theValue = ($theValue != "") ? (filter_var($theValue, FILTER_VALIDATE_EMAIL))   : "NULL";
            break;
            case "long":
            case "int":
                $theValue = ($theValue != "") ? intval($theValue) : "NULL";
                break;
            case "double":
                $theValue = ($theValue != "") ? doubleval($theValue) : "NULL";
                break;
            case "date":
                $theValue = ($theValue != "") ? "'" . $theValue . "'" : "NULL";
                break;
            case "defined":
                $theValue = ($theValue != "") ? $theDefinedValue : $theNotDefinedValue;
                break;
        }
        return $theValue;
    }



}